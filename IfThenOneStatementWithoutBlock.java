public class IfThenOneStatementWithoutBlock {
    public static void main (String[] args){
        int number = 2;//[1]
        if (number == 1)//[2]
            System.out.println("[3] number = 1");
        if (number == 2)//[4]
            System.out.println("[5] number = 2");
        if (number == 3)//[6]
            System.out.println("[7] number = 3");
    }
}
